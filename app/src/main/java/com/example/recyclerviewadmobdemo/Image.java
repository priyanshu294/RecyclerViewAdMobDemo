package com.example.recyclerviewadmobdemo;

public class Image {

    private  String image , image_value;

    public Image(String image, String image_value) {
        this.image = image;
        this.image_value = image_value;
    }

    public String getImage() {
        return image;
    }

    public String getImage_value() {
        return image_value;
    }
}
